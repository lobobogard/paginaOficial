<script lang="ts" setup>
// modal
let showModal = ref(false)
let showModalEnvio = ref('animate__animated animate__fadeIn')

function toggleModal() {
    showModalEnvio.value = 'animate__animated animate__fadeOut'
    setTimeout(() => { showModal.value = false  }, 400);
}

// data
let messages = ref('')
let errors = ref([])
let statusCode = ref(0) 
let nombre = ref(null)
let correo = ref('')
let mensaje = ref(null)

let classNombre = ref('animate__animated animate__fadeOut')
let classCorreo = ref('animate__animated animate__fadeOut')
let classMensaje = ref('animate__animated animate__fadeOut')

let validacion = ref(true)
let errorCorreo = ref('')
let spinner = ref(true)


const enviarCorreo = () => { 
    if (validarformulario()) {
        enviar()
    }
}

function enviar() {
    spinner.value = false
    const { data, error } = useFetch(
    'http://0.0.0.0/api/enviarCorreoPaginaOficial',
    {
      method: 'POST',
      body: {
        nombre: nombre.value,
        correo: correo.value,
        mensaje: mensaje.value,
      },
      headers: {
        'Content-type': 'application/json; charset=UTF-8',
      },
      onResponse({ response }) {
        statusCode.value = response.status
        
        if (statusCode.value == 200) {
            messages.value = response._data.message
            limpiar()
        } else {
            // alert(response._data.message[0])
            errors.value = response._data.message
        }

        showModalEnvio.value = 'animate__animated animate__fadeIn'
        showModal.value = true
        spinner.value = true
      }
    })
}

function validarformulario():boolean {
    validacion.value = true
    if (nombre.value == null || nombre.value == "") {
        classNombre.value = 'animate__animated animate__fadeInRight'  
        validacion.value = false
    } else {
        classNombre.value = 'animate__animated animate__fadeOutRight'
    }

    if (correo.value == null || correo.value == "") {
        errorCorreo.value = 'correo obligatorio'
        classCorreo.value = 'animate__animated animate__fadeInRight'  
        validacion.value = false
    } else {
        if (validaEmail()) {
          errorCorreo.value = 'correo inválido'
          classCorreo.value = 'animate__animated animate__fadeInRight' 
          validacion.value = false 
        } else {
            classCorreo.value = 'animate__animated animate__fadeOutRight'
        }
    }

    if (mensaje.value == null || mensaje.value == "") {
        classMensaje.value = 'animate__animated animate__fadeInRight'  
        validacion.value = false
    } else {
        classMensaje.value = 'animate__animated animate__fadeOutRight'
    }

    return validacion.value
}

function validaEmail():boolean {
    var validRegex = /^[^@]+@\w+(\.\w+)+\w$/;
    if (validRegex.test(correo.value)) {
      return false;
    } else {
      return true;
    }
}

function limpiar() {
    nombre.value = null
    correo.value = ''
    mensaje.value = null
}

